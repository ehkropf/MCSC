%% unbounded 5 polygon example
clear


%% create polygon domain
P = extpolys(...
  [-1.93-0.76i -2.65+0.76i -3.57+0.76i -3.54-1.3i],...
  [-.64-1.67i -1.79-2.21i -2.07-2.91i 1.37-3.05i -.29-2.14i],...
  [.55-1.29i 2.07-2i 3.31-1.06i 1.9-0.08i .5-.18i],...
  [1.06+.85i 3.29+1.39i 3.17+2.44i 1.09+2.8i 1.56+1.56i],...
  [-1.06+0.43i .15+1.7i -2.42+1.88i] ...
);


%% circle domain
Cg = circdomain(...
  { 0 1 [ 0; pi/3; 2*pi/3; 4*pi/3 ] },...
  { 1.5-2.4i 1 [ pi/2; pi; 5*pi/4; 7*pi/4; pi/4 ] },...
  { 3.5-0.9i 1 [ pi; 3*pi/2; 0; pi/2; 3*pi/4 ] },...
  { 5.4+1.8i 1 [ 5*pi/4; 7*pi/4; pi/4; 3*pi/4; pi ] },...
  { 1.9+1.4i 1 [ 3*pi/2; pi/4; 3*pi/4 ] } ...
);


%% create map object
opts = extmapopts;
opts.method = 'fpls';
opts.monitor = 1;

f = extmap(P, circdomain(Cg), opts);

